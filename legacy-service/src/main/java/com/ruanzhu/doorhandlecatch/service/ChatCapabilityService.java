package com.ruanzhu.doorhandlecatch.service;

import org.springframework.stereotype.Service;

@Service
public class ChatCapabilityService {

    public String buildCapabilityOverview() {
        return """
                我可以覆盖系统内大部分查询、解释和辅助操作，按业务域分为：

                1. 检测任务与工业质检闭环
                - 创建/上传检测任务、启动检测、查询任务进度、查看检测结果和结果图。
                - 查询采集端、批次、工单、采集人、设备维度的检测记录和任务流转。
                - 进入缺陷证据库，按缺陷类型、严重等级、设备、批次、模型筛选图片证据链。
                - 查看缺陷类型、位置、面积、置信度、严重等级、复核结论和误报反馈。
                - 查询质检队列、待复核、待处置、返工、复检、失败任务，并定位责任人与站点。
                - 生成任务质检报告、批次追溯报告、工单追溯报告；涉及写操作时会先要求确认。

                2. 设备、人员和模型资源管理
                - 查询设备列表、设备状态、未分配设备、设备绑定人员、设备采集告警。
                - 查询人员/员工信息、部门、岗位类型、状态、人员绑定设备。
                - 查询模型版本、默认模型、发布状态、使用次数、验证状态。
                - 查看模型评估指标、推理性能、灰度发布、A/B 分组、回滚信息。
                - 创建设备/人员/模型等复杂表单写操作会先整理变更说明，不直接静默落库。

                3. 报表与数据分析
                - 生成检测总览、趋势、区域统计、模型统计、缺陷分布。
                - 生成质量处置统计、质检工作量、设备/人员维度分析。
                - 支持日报、周报、摘要、异常分析、严重缺陷优先级分析和闭环率分析。

                4. 系统运维与平台状态
                - 检查 OSS、Kafka、远程 Worker、失败任务和系统基础运行状态。
                - 解释任务卡住、上传失败、检测失败、结果未回传等常见问题。

                5. 对话与项目协作
                - 支持多轮上下文、会话管理、项目归档、对话流式回答。
                - 支持 Agent checkpoint、路由轨迹、循环守卫和健康状态查看，便于定位异常对话。
                - 可以把复杂需求拆成步骤，并告诉你下一步应该在哪个页面或接口完成。

                当前限制：
                - 查询、解释、汇总类能力覆盖较完整。
                - 高风险写操作会走确认流程或给出可执行说明，不会直接绕过表单校验。
                - 复杂产线实时控制、PLC 指令下发、报表 PDF/Word 导出和真实边缘采集协议还需要继续接入专门接口。
                """;
    }
}
